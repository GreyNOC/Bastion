"""Report Center — render a BastionReport to every supported format.

Formats: JSON, Markdown, HTML (dark, printable), CSV, SARIF 2.1.0, PDF, and an
evidence bundle (delegated to the Evidence Center). Every renderer runs finding
text through the secret scrubber as a final backstop, so no full secret can
appear in output even if an upstream producer forgot to mask.
"""

from __future__ import annotations

import csv
import io
import json
from pathlib import Path

from ..safety.masking import scrub_text
from ..schemas import BastionFinding, BastionReport, ReportFormat
from ..utils.logging import get_logger

_SEV_COLOR = {
    "critical": "#ff4d4f",
    "high": "#ff9f43",
    "medium": "#ffd93d",
    "low": "#4dc9ff",
    "info": "#8a94a6",
}


class ReportCenter:
    """Renders reports. Stateless; safe to construct per call."""

    def __init__(self) -> None:
        self.log = get_logger("report_center")

    # --- public API ----------------------------------------------------------
    def render(self, report: BastionReport, fmt: ReportFormat) -> str | bytes:
        report.recompute_summary()
        if fmt == ReportFormat.JSON:
            return self.to_json(report)
        if fmt == ReportFormat.MARKDOWN:
            return self.to_markdown(report)
        if fmt == ReportFormat.HTML:
            return self.to_html(report)
        if fmt == ReportFormat.CSV:
            return self.to_csv(report)
        if fmt == ReportFormat.SARIF:
            return self.to_sarif(report)
        if fmt == ReportFormat.PDF:
            return self.to_pdf(report)
        raise ValueError(f"unsupported format: {fmt}")

    def write(self, report: BastionReport, out_dir: Path,
              formats: list[ReportFormat]) -> dict[str, str]:
        """Render and write each format to ``out_dir``. Returns format->path."""
        out_dir = Path(out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)
        report.recompute_summary()
        written: dict[str, str] = {}
        ext = {
            ReportFormat.JSON: "json", ReportFormat.MARKDOWN: "md",
            ReportFormat.HTML: "html", ReportFormat.CSV: "csv",
            ReportFormat.SARIF: "sarif.json", ReportFormat.PDF: "pdf",
        }
        for fmt in formats:
            if fmt == ReportFormat.EVIDENCE_BUNDLE:
                continue  # handled by the Evidence Center
            try:
                content = self.render(report, fmt)
                path = out_dir / f"{report.report_id}.{ext[fmt]}"
                if isinstance(content, bytes):
                    path.write_bytes(content)
                else:
                    path.write_text(content, encoding="utf-8")
                written[fmt.value] = str(path)
            except Exception as exc:  # noqa: BLE001 - one bad format must not sink the rest
                self.log.warning("failed to render %s: %s", fmt.value, exc)
        report.formats = [f for f in formats if f.value in written]
        report.output_paths = written
        return written

    # --- renderers -----------------------------------------------------------
    def to_json(self, report: BastionReport) -> str:
        # Scrub the serialized document as a final backstop: even if a producer
        # left secret-shaped text in a finding, it never reaches the JSON file.
        return scrub_text(json.dumps(report.to_dict(), indent=2, ensure_ascii=False))

    def to_markdown(self, report: BastionReport) -> str:
        s = report.summary
        lines = [
            f"# {report.title}",
            "",
            f"- **Generated:** {report.generated_at}",
            f"- **Generated by:** {report.generated_by}",
            f"- **Modules:** {', '.join(report.modules) or '—'}",
            f"- **Report ID:** `{report.report_id}`",
            "",
            "## Executive summary",
            "",
            s.headline,
            "",
            "| Severity | Count |",
            "| --- | --- |",
        ]
        for sev in ("critical", "high", "medium", "low", "info"):
            if s.by_severity.get(sev):
                lines.append(f"| {sev} | {s.by_severity[sev]} |")
        lines += ["", "## Findings", ""]
        for f in sorted(report.findings, key=lambda x: x.priority_score, reverse=True):
            lines += self._finding_markdown(f)
        lines += ["", "---", "", "_GreyNOC Bastion — local-first defensive report. "
                  "No full secrets are included. Findings are evidence-backed; "
                  "validate drafts before acting._"]
        return scrub_text("\n".join(lines))

    def _finding_markdown(self, f: BastionFinding) -> list[str]:
        out = [
            f"### [{f.severity.value.upper()}] {f.title}",
            "",
            f"- **Confidence:** {f.confidence.value}",
            f"- **Category:** {f.category.value}",
            f"- **Source:** {f.source}",
            f"- **Affected:** `{f.affected}`",
            f"- **Validation status:** {f.validation_status.value}",
            f"- **Correlation ID:** `{f.correlation_id}`",
            f"- **Timestamp:** {f.timestamp}",
            "",
            f"**Why it matters:** {f.why_it_matters}",
            "",
            f"**Recommended action:** {f.recommended_action}",
        ]
        if f.evidence:
            out += ["", "**Evidence:**"]
            for ev in f.evidence:
                out.append(f"- {ev.short()}")
        if f.false_positive_notes:
            out += ["", f"**False-positive notes:** {f.false_positive_notes}"]
        if f.operator_notes:
            out += ["", f"**Operator notes:** {f.operator_notes}"]
        out.append("")
        return out

    def to_csv(self, report: BastionReport) -> str:
        buf = io.StringIO()
        writer = csv.writer(buf)
        writer.writerow([
            "correlation_id", "title", "severity", "confidence", "category",
            "source", "affected", "validation_status", "why_it_matters",
            "recommended_action", "false_positive_notes", "timestamp",
        ])
        s = _csv_safe  # neutralize spreadsheet formula injection
        for f in sorted(report.findings, key=lambda x: x.priority_score, reverse=True):
            writer.writerow([
                s(f.correlation_id), s(scrub_text(f.title)), f.severity.value,
                f.confidence.value, f.category.value, s(scrub_text(f.source)),
                s(scrub_text(f.affected)), f.validation_status.value,
                s(scrub_text(f.why_it_matters)), s(scrub_text(f.recommended_action)),
                s(scrub_text(f.false_positive_notes)), f.timestamp,
            ])
        return buf.getvalue()

    def to_sarif(self, report: BastionReport) -> str:
        """SARIF 2.1.0 — one run, rules derived from finding titles."""
        from .. import __version__
        sev_to_level = {"critical": "error", "high": "error", "medium": "warning",
                        "low": "note", "info": "note"}
        rules: dict[str, dict] = {}
        results = []
        for f in report.findings:
            rule_id = f.ref_id or f.category.value + ":" + (f.title[:40] or "finding")
            if rule_id not in rules:
                rules[rule_id] = {
                    "id": rule_id,
                    "name": scrub_text(f.title)[:120] or rule_id,
                    "shortDescription": {"text": scrub_text(f.title)[:200]},
                    "fullDescription": {"text": scrub_text(f.why_it_matters)[:1000]},
                    "defaultConfiguration": {"level": sev_to_level.get(f.severity.value, "warning")},
                }
            results.append({
                "ruleId": rule_id,
                "level": sev_to_level.get(f.severity.value, "warning"),
                "message": {"text": scrub_text(f.why_it_matters or f.title)},
                "properties": {
                    "severity": f.severity.value,
                    "confidence": f.confidence.value,
                    "validation_status": f.validation_status.value,
                    "correlation_id": f.correlation_id,
                    "recommended_action": scrub_text(f.recommended_action),
                },
                "locations": [{
                    "physicalLocation": {
                        "artifactLocation": {"uri": self._as_uri(f.affected)},
                    }
                }] if f.affected else [],
            })
        doc = {
            "$schema": "https://json.schemastore.org/sarif-2.1.0.json",
            "version": "2.1.0",
            "runs": [{
                "tool": {"driver": {
                    "name": "GreyNOC Bastion",
                    "informationUri": "https://greynoc.local/bastion",
                    "version": __version__,
                    "rules": list(rules.values()),
                }},
                "results": results,
            }],
        }
        return json.dumps(doc, indent=2, ensure_ascii=False)

    @staticmethod
    def _as_uri(affected: str) -> str:
        # Scrub: `affected` is free-form producer text (repo path, host:port,
        # connection string) and is the one SARIF field not otherwise scrubbed.
        a = scrub_text((affected or "").strip())
        if not a:
            return "unknown"
        return a.replace("\\", "/")

    def to_html(self, report: BastionReport) -> str:
        return render_html_report(report)

    def to_pdf(self, report: BastionReport) -> bytes:
        """Render a simple, valid multi-page PDF with no third-party deps."""
        return _text_to_pdf(_report_plaintext(report))


# --- CSV formula-injection neutralization -----------------------------------
# A cell whose first character is one of these is interpreted as a formula by
# Excel / LibreOffice / Google Sheets. Prefix such cells with a single quote so
# the spreadsheet treats them as literal text. See OWASP "CSV Injection".
_CSV_TRIGGERS = ("=", "+", "-", "@", "\t", "\r")


def _csv_safe(value) -> str:
    text = "" if value is None else str(value)
    if text[:1] in _CSV_TRIGGERS:
        return "'" + text
    return text


# --- HTML rendering (shared with the dashboard) -----------------------------
def _esc(text: str) -> str:
    return (
        str(text)
        .replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def render_html_report(report: BastionReport) -> str:
    report.recompute_summary()
    s = report.summary
    sev_chips = "".join(
        f'<span class="chip" style="--c:{_SEV_COLOR[k]}">{k}: {s.by_severity[k]}</span>'
        for k in ("critical", "high", "medium", "low", "info") if s.by_severity.get(k)
    )
    rows = []
    for f in sorted(report.findings, key=lambda x: x.priority_score, reverse=True):
        ev_html = "".join(f"<li>{_esc(scrub_text(e.short()))}</li>" for e in f.evidence)
        rows.append(f"""
        <details class="finding sev-{f.severity.value}">
          <summary>
            <span class="badge" style="--c:{_SEV_COLOR.get(f.severity.value, '#888')}">{_esc(f.severity.value)}</span>
            <span class="ftitle">{_esc(scrub_text(f.title))}</span>
            <span class="fmeta">{_esc(f.category.value)} · {_esc(f.confidence.value)} confidence · {_esc(f.validation_status.value)}</span>
          </summary>
          <div class="fbody">
            <p><strong>Affected:</strong> <code>{_esc(scrub_text(f.affected))}</code></p>
            <p><strong>Why it matters:</strong> {_esc(scrub_text(f.why_it_matters))}</p>
            <p><strong>Recommended action:</strong> {_esc(scrub_text(f.recommended_action))}</p>
            {'<p><strong>Evidence:</strong></p><ul>' + ev_html + '</ul>' if ev_html else ''}
            {'<p class="fp"><strong>False-positive notes:</strong> ' + _esc(scrub_text(f.false_positive_notes)) + '</p>' if f.false_positive_notes else ''}
            <p class="cid">Correlation ID: <code>{_esc(f.correlation_id)}</code> · {_esc(f.timestamp)} · source: {_esc(scrub_text(f.source))}</p>
          </div>
        </details>""")

    findings_html = "\n".join(rows) or '<p class="empty">No findings.</p>'
    return f"""<!doctype html>
<html lang="en"><head><meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>{_esc(report.title)}</title>
<style>{REPORT_CSS}</style></head>
<body>
<div class="report">
  <header>
    <div class="brand">GreyNOC <b>Bastion</b></div>
    <h1>{_esc(report.title)}</h1>
    <div class="meta">Generated {_esc(report.generated_at)} · modules: {_esc(', '.join(report.modules) or '—')} · <code>{_esc(report.report_id)}</code></div>
  </header>
  <section class="summary">
    <h2>Executive summary</h2>
    <p class="headline">{_esc(s.headline)}</p>
    <div class="chips">{sev_chips or '<span class="chip" style="--c:#4caf50">no findings</span>'}</div>
  </section>
  <section class="findings">
    <h2>Findings <span class="count">{len(report.findings)}</span></h2>
    {findings_html}
  </section>
  <footer>
    GreyNOC Bastion — local-first defensive report. No full secrets are included.
    Findings are evidence-backed; validate drafts before acting.
  </footer>
</div>
</body></html>"""


