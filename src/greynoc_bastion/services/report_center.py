def _report_plaintext(report: BastionReport) -> str:
    report.recompute_summary()
    lines = [
        report.title,
        "=" * len(report.title),
        f"Generated: {report.generated_at}",
        f"Report ID: {report.report_id}",
        f"Modules: {', '.join(report.modules) or '-'}",
        "",
        "EXECUTIVE SUMMARY",
        report.summary.headline,
        "",
        "FINDINGS",
    ]
    for f in sorted(report.findings, key=lambda x: x.priority_score, reverse=True):
        lines += [
            "",
            f"[{f.severity.value.upper()}] {f.title}",
            f"  Confidence: {f.confidence.value} | Category: {f.category.value} | Status: {f.validation_status.value}",
            f"  Affected: {f.affected}",
            f"  Why it matters: {f.why_it_matters}",
            f"  Recommended action: {f.recommended_action}",
            f"  Correlation ID: {f.correlation_id}",
        ]
        for ev in f.evidence[:5]:
            lines.append(f"    - {ev.short()}")
    lines += ["", "GreyNOC Bastion — local-first defensive report. No full secrets included."]
    return scrub_text("\n".join(lines))


def _text_to_pdf(text: str) -> bytes:
    """Minimal, dependency-free multi-page PDF writer for monospaced text.

    Produces a valid PDF 1.4 with Helvetica, wrapping long lines and paginating.
    Good enough for evidence-grade text reports without pulling in a PDF library.
    """
    def esc(s: str) -> str:
        return s.replace("\\", r"\\").replace("(", r"\(").replace(")", r"\)")

    # Wrap + paginate.
    max_chars = 92
    lines_per_page = 58
    raw_lines: list[str] = []
    for line in text.replace("\t", "    ").split("\n"):
        line = "".join(ch if 32 <= ord(ch) < 127 else "?" for ch in line)
        if not line:
            raw_lines.append("")
            continue
        while len(line) > max_chars:
            raw_lines.append(line[:max_chars])
            line = line[max_chars:]
        raw_lines.append(line)

    pages: list[list[str]] = [raw_lines[i:i + lines_per_page]
                              for i in range(0, len(raw_lines), lines_per_page)] or [[""]]

    objects: list[bytes] = []

    def add(obj: bytes) -> int:
        objects.append(obj)
        return len(objects)

    font_obj = add(b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>")
    page_obj_ids: list[int] = []
    content_obj_ids: list[int] = []
    for page in pages:
        stream_lines = ["BT", "/F1 10 Tf", "12 TL", "50 780 Td"]
        for ln in page:
            stream_lines.append(f"({esc(ln)}) Tj")
            stream_lines.append("T*")
        stream_lines.append("ET")
        stream = "\n".join(stream_lines).encode("latin-1", "replace")
        content_id = add(b"<< /Length %d >>\nstream\n%s\nendstream" % (len(stream), stream))
        content_obj_ids.append(content_id)
        page_obj_ids.append(-1)  # placeholder, fixed below

    pages_parent_id = len(objects) + len(pages) + 1  # reserve
    # Create page objects now that we know the parent id.
    real_page_ids: list[int] = []
    for content_id in content_obj_ids:
        pid = add(
            b"<< /Type /Page /Parent %d 0 R /MediaBox [0 0 612 792] "
            b"/Resources << /Font << /F1 %d 0 R >> >> /Contents %d 0 R >>"
            % (pages_parent_id, font_obj, content_id)
        )
        real_page_ids.append(pid)
    kids = " ".join(f"{pid} 0 R" for pid in real_page_ids).encode()
    pages_id = add(b"<< /Type /Pages /Count %d /Kids [%s] >>" % (len(real_page_ids), kids))
    catalog_id = add(b"<< /Type /Catalog /Pages %d 0 R >>" % pages_id)

    # Assemble file with xref.
    out = io.BytesIO()
    out.write(b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n")
    offsets = [0] * (len(objects) + 1)
    for i, obj in enumerate(objects, start=1):
        offsets[i] = out.tell()
        out.write(b"%d 0 obj\n" % i)
        out.write(obj)
        out.write(b"\nendobj\n")
    xref_pos = out.tell()
    out.write(b"xref\n")
    out.write(b"0 %d\n" % (len(objects) + 1))
    out.write(b"0000000000 65535 f \n")
    for i in range(1, len(objects) + 1):
        out.write(b"%010d 00000 n \n" % offsets[i])
    out.write(b"trailer\n<< /Size %d /Root %d 0 R >>\n" % (len(objects) + 1, catalog_id))
    out.write(b"startxref\n%d\n%%%%EOF\n" % xref_pos)
    return out.getvalue()
